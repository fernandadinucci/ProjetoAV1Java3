package Estrutura;

public class Carro {
	private String chassi; 
	private String montadora;  
	private String modelo; 
	private String cor; 
	private float motorização; 
	private String câmbio; 
	private float preço;
	private String tipo;
	
	public String getChassi() {
		return chassi;
	}
	public void setChassi(String chassi) {
		this.chassi = chassi;
	}
	public String getMontadora() {
		return montadora;
	}
	public void setMontadora(String montadora) {
		this.montadora = montadora;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public float getMotorização() {
		return motorização;
	}
	public void setMotorização(float motorização) {
		this.motorização = motorização;
	}
	public String getCâmbio() {
		return câmbio;
	}
	public void setCâmbio(String câmbio) {
		this.câmbio = câmbio;
	}
	public float getPreço() {
		return preço;
	}
	public void setPreço(float preço) {
		this.preço = preço;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}  
}
